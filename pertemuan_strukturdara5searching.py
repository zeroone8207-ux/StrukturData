#binary search
def binarySearch(arr, l, r, x):
  while l <= r:
    mid = l + (r - l) // 2

    #check if x is present at mid
    if arr[mid] == x:
      return mid

    #if x is greater, ignore left half
    elif arr[mid] < x:
      l = mid + 1
    #if x is smaller, ignore right half
    else:
      r = mid - 1

  #if we reach here, then the element wasn't present
  return -l

