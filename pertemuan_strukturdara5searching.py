class pair:
  def __init__(self) -> None:
    self.min = 0
    self.max = 0

def getminmax(arr: list[int], n: int) -> pair:
  minmax = pair()

  if n == 1:
    minmax.max = arr[0]
    minmax.min = arr[0]
    return minmax

  if arr[0] > arr[1]:
    minmax.max = arr[0]
    minmax.min = arr[1]
  else:
    minmax.max = arr[1]
    minmax.min = arr[0]

  for i in range(2, n):
    if arr[i] > minmax.max:
      minmax.max = arr[i]
    elif arr[i] < minmax.min:
      minmax.min = arr[i]

  return minmax
