def is_connected(graph):
    if not graph:
        return True
    visited = set()

    def dfs(node):
        visited.add(node)
        for neighbor in graph.get(node, []):
            if neighbor not in visited:
                dfs(neighbor)
    start_node = next(iter(graph))
    dfs(start_node)

    return len(visited) == len(graph)

