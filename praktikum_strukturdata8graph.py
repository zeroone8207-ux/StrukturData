def find_path(graph, start, end, path=[]):
  path = path + [start]
  if start == end:
    return path
  if start not in graph:
      return None
  for neighbor in graph[start]:
      if neighbor not in path:
          new_path = find_path(graph, neighbor, end, path)
          if new_path:
            return new_path
  return None


